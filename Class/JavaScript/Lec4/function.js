//Function 
//Function without Parameters
const greet = function() {
    return "Hello World!";
};
console.log(greet());

//Function with Return Value
const add = function(a, b) {
  return a + b;
};

add(4, 5);
console.log(add(4, 5));

//Function with Parameters
const multiply = function(x, y) {
    return x * y;
};

console.log(multiply(3, 7));
